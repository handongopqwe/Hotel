package entity;

public class khCourse {
  private String  xm;
  private String  xb;
  private String  ch;
  private String  sfid;
  private String  zz;
public khCourse() {
	super();
}
public khCourse(String xm, String xb, String ch, String sfid, String zz) {
	super();
	this.xm = xm;
	this.xb = xb;
	this.ch = ch;
	this.sfid = sfid;
	this.zz = zz;
}
public String getXm() {
	return xm;
}
public void setXm(String xm) {
	this.xm = xm;
}
public String getXb() {
	return xb;
}
public void setXb(String xb) {
	this.xb = xb;
}
public String getCh() {
	return ch;
}
public void setCh(String ch) {
	this.ch = ch;
}
public String getSfid() {
	return sfid;
}
public void setSfid(String sfid) {
	this.sfid = sfid;
}
public String getZz() {
	return zz;
}
public void setZz(String zz) {
	this.zz = zz;
}
  
  
}
